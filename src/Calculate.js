// Currently have basic functions for MVP.
// Want to add complex factor for levels (K - 5).
export default class Calculate {
  static add(x, y) {
    return x + y;
  }

  static subtract(x, y) {
    return x - y;
  }

  static multiply(x, y) {
    return x * y;
  }

  static divide(x, y) {
    return x / y;
  }
}
